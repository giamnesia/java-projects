
public class compute {

    public static double add, subtraction, multiply, divide;
    public static double num1, num2;
    public static void add (double setNum1, double setNum2){
        num1=setNum1;
        num2=setNum2;
        add= num1+num2;
    }
    public static void subtraction (double setNum1, double setNum2){
        num1=setNum1;
        num2=setNum2;
        subtraction= num1-num2;
    }
    public static void multiply (double setNum1, double setNum2){
        num1=setNum1;
        num2=setNum2;
        multiply =num1*num2;
    }
    public static void divide (double setNum1, double setNum2){
        num1=setNum1;
        num2=setNum2;
        divide =num1/num2;
    }

}
